// ---- Main emitter ----

export function emitTypeScript(sdk: SdkDefinition, outDir: string): void {
  const srcDir = join(outDir, "src");
  const nsDir = join(srcDir, "namespaces");
  mkdirSync(nsDir, { recursive: true });

  // Static files
  writeFileSync(join(srcDir, "types.ts"), TYPES_TS);
  writeFileSync(join(srcDir, "errors.ts"), ERRORS_TS);
  writeFileSync(join(srcDir, "transport.ts"), TRANSPORT_TS);

  // Namespace files
  for (const ns of sdk.namespaces) {
    const emitter = NS_EMITTERS[ns.id];
    if (!emitter) {
      console.warn(`No TypeScript emitter for namespace: ${ns.id}`);
      continue;
    }
    const filename = NS_FILENAMES[ns.id] ?? `${ns.id}.ts`;
    writeFileSync(join(nsDir, filename), emitter(ns));
  }

  // Client
  writeFileSync(join(srcDir, "client.ts"), emitClient(sdk));

  // Index
  writeFileSync(join(srcDir, "index.ts"), emitIndex(sdk));

  console.log(`  TypeScript: wrote ${7 + sdk.namespaces.length} files to ${srcDir}`);
}
